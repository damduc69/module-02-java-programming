package Variable_DataType;

public class VariableDataType {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5d;
        boolean b = true;
        char c = 'c';
        String str = "Hồ Chí Minh";

        System.out.printf("i = " + i + "\n");
        System.out.printf("f = " + f + "\n");
        System.out.printf("d = " + d + "\n");
        System.out.printf("b = " + b + "\n");
        System.out.printf("c = " + c + "\n");
        System.out.printf("str = " + str + "\n");
    }
}
